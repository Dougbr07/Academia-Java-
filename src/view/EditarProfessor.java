        public void excluirProf() throws SQLException, Exception {
        int resp = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir esse professor?",
                "Confirmação", JOptionPane.YES_NO_OPTION);
        if (resp == JOptionPane.YES_NO_OPTION) {
            ProfessorDao dao = new ProfessorDao();
            dao.excluir(professores.get(jTTabela.getSelectedRow()));
            mostraPesquisa(professores);
        }
    }

    private void listarProfessores() throws Exception {
        
       ProfessorDao dao = new ProfessorDao();
       professores = dao.consultarTodos("%" + jTPesquisa.getText()+ "%"); 
       mostraPesquisa(professores);
        
  
    }
    
    private void mostraPesquisa(List<Professor> professores) {
     while(tmProfessor.getRowCount()>0){
            tmProfessor.removeRow(0);
        }
      if(professores.isEmpty()){
          JOptionPane.showMessageDialog(null, "Nenhum professor cadastrado!");
      }else{
          String[] linha = new String[] {null, null, null, null, null, null};
          for (int i = 0; i < professores.size(); i++) {
              tmProfessor.addRow(linha);
              tmProfessor.setValueAt(professores.get(i).getIdProf(), i, 0);
              tmProfessor.setValueAt(professores.get(i).getNome(), i, 1);
              tmProfessor.setValueAt(professores.get(i).getCpf(), i, 2);
              tmProfessor.setValueAt(professores.get(i).getEndereco(), i, 3);
              tmProfessor.setValueAt(professores.get(i).getTelefone(), i, 4);
              tmProfessor.setValueAt(professores.get(i).getSalario(), i, 5);
          }
      }  
    }

    
    private void jTTabelaLinhaSelecionada(JTable tabela){
        if (jTTabela.getSelectedRow() != -1) {
            habilitaDados();
            JTid.setText(String.valueOf(professores.get(tabela.getSelectedRow()).getIdProf()));
            JTnome.setText(professores.get(tabela.getSelectedRow()).getNome());
            JTend.setText(professores.get(tabela.getSelectedRow()).getEndereco());
            JTtel.setText(professores.get(tabela.getSelectedRow()).getTelefone());
            JTsalario.setText(String.valueOf(professores.get(tabela.getSelectedRow()).getSalario()));
            JTcpf.setText(String.valueOf(professores.get(tabela.getSelectedRow()).getCpf()));
            popularList();

            
        } else {
            limparCampos();
            model.removeAllElements();   
            jListAulasDadas.setModel(model);

        }

 
}
    public void popularList (){
        model.removeAllElements();   
        jListAulasDadas.setModel(model);  
        
        PreparedStatement stmt = null;
        Connection conn = null;
        
        try {
            conn = ConexaoBanco.getAbreConexao();
            stmt = conn.prepareStatement("select aula.horario, aula.nome from professor\n" +
                                         "inner join aula\n" +
                                         "on aula.id_prof = professor.id_prof\n" +
                                         "where professor.id_prof = "+ JTid.getText());
            ResultSet rs = stmt.executeQuery();

            
            
            while ( rs.next()){
                String aulas = rs.getString("horario")+" - "+rs.getString("nome");
                model.addElement(aulas);
  
               
            }
            }   catch (SQLException | HeadlessException e ) {
            JOptionPane.showMessageDialog (null, " Erro   \n" +e);
        }
    
    } 

