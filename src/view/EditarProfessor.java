     private void alteraProfessor() throws SQLException, Exception {
       if(jTTabela.getSelectedRow()!=-1){
           if(verificadDados()){
               Professor professor = new Professor();
               ProfessorDao dao = new ProfessorDao();
              
               professor.setIdProf(Integer.parseInt(JTid.getText()));
               professor.setNome(JTnome.getText());
               professor.setEndereco(JTend.getText());
               professor.setTelefone(JTtel.getText());
               professor.setCpf(JTcpf.getText());
               professor.setSalario(Float.parseFloat(JTsalario.getText()));

               
               dao.alterar(professor);
               JOptionPane.showMessageDialog(null, "Professor alterado com sucesso!");
           }
           
       }
    } 
        
