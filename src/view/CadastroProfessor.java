    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    
      if(verificadDados()){
        Professor novoProfessor = new Professor(nomeProfessorText.getText(),cpfFormattedText.getText());
        
     //   novoProfessor.setNome(nomeProfessorText.getText());
        novoProfessor.setEndereco(enderecoProfessorText.getText());
     //   novoProfessor.setCpf(cpfFormattedText.getText());
        novoProfessor.setSalario((float) Double.parseDouble(salarioProfessorText.getText()));
        novoProfessor.setTelefone(telefoneFormattedTextField.getText());
        

     ProfessorController professorController = new ProfessorController();
      boolean resultado = professorController.cadastrar(novoProfessor);
      
      if(resultado) {
	JOptionPane.showMessageDialog(null, "Professor cadastrado com sucesso!");
	} else {
	JOptionPane.showMessageDialog(null, "Erro ao cadastrar professor!");
	}

        
        nomeProfessorText.setText("");
        enderecoProfessorText.setText("");
        cpfFormattedText.setText("");
        salarioProfessorText.setText("");
        telefoneFormattedTextField.setText("");
      }
    }//GEN-LAST:event_jButton1ActionPerformed

