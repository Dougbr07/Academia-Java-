    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(verificadDados()){ 
        String REGEX = "\\d+";
        Matcher matcher = Pattern.compile(REGEX).matcher(comboBox.getSelectedItem().toString());
        matcher.find();        
        Aula novaAula = new Aula(nomeAulaText.getText(),Float.parseFloat(precoAulaText.getText()));
        
        //novaAula.setNome(nomeAulaText.getText());
        // novaAula.setPreco(Float.parseFloat(precoAulaText.getText()));
        novaAula.setHorario(horaFormattedTextField.getText());
        novaAula.setIdProf(Integer.parseInt(matcher.group()));
        
      AulaController aulaController = new AulaController();
      boolean resultado = aulaController.cadastrar(novaAula);
      
      if(resultado) {
	JOptionPane.showMessageDialog(null, "Aula cadastrada com sucesso!");
	} else {
	JOptionPane.showMessageDialog(null, "Erro ao cadastrar aula!");
	}
        
        nomeAulaText.setText("");
        precoAulaText.setText("");
        horaFormattedTextField.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    
    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        
        PreparedStatement stmt = null;
        Connection conn = null;
        
        try {
            conn = ConexaoBanco.getAbreConexao();
            stmt = conn.prepareStatement("select id_prof, nome from professor");
            ResultSet rs = stmt.executeQuery();

            
            
            while ( rs.next()){
                String prof = rs.getString("id_prof")+"-"+rs.getString("nome");
                comboBox.addItem(prof);
  
               
            }
            }   catch (SQLException | HeadlessException e ) {
            JOptionPane.showMessageDialog (null, " Erro   \n" +e);
        }
        
        
    }//GEN-LAST:event_formWindowOpened

